#!/bin/python
import os
import sys
import csv

sys.path.append(os.path.join(os.path.dirname(__file__), 'common'))
from common import utils

script_name = sys.argv[0]
rom_file = sys.argv[1]
output_file = sys.argv[2]

THRESHOLD = 0x20

with open(rom_file, 'rb') as rfp, open(output_file, 'w', encoding='utf-8') as ofp:
    ofp.write(f'; File autogenerated by {script_name}\n')

    for bank in range(1, 0xFF):
        # Note the upper limit of the current bank
        bank_start = utils.rom2realaddr((bank, 0x4000))
        # Seek to the end of the bank
        rfp.seek(bank_start + 0x3FFF - THRESHOLD, os.SEEK_SET)
        size = 0
        while rfp.read(THRESHOLD) == bytearray(THRESHOLD):
            if rfp.seek(-(2 * THRESHOLD), os.SEEK_CUR) > bank_start:
                size += THRESHOLD
            else:
                break

        if size > THRESHOLD:
            ofp.write(f'\nSECTION "Free Space Bank {bank:02X}", ROMX[${0x8000 - size:04X}], BANK[${bank:02X}]\n')
            ofp.write(f'FreeSpaceBank{bank:02X}::\n')
            ofp.write(f'REPT $8000 - FreeSpaceBank{bank:02X}\n')
            ofp.write(f'  db 0\n')
            ofp.write(f'ENDR\n')